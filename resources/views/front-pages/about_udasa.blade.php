              <p class="">
              The University of Dar es Salaam is the oldest and biggest public university in Tanzania. It was established on 1st July 1970,
               through parliament Act no. 12 of 1970 and all the enabling legal instruments of the constituent colleges. Prior to 1970, 
               the University college, Dar es Salaam had started on 1st July 1961 as an affiliate college of the University of London. 
               It had only one faculty- the faculty of Law, with 13 students. In 1963 it became a constituent college of the University of
                East Africa together with Makerere University College in Uganda and Nairobi University College in Kenya. Since 1961,
               the University of Dar es Salaam has grown in terms of student intake, academic units, and academic programmes. 
              </p>
              <p>
              In the early 1990s, transformation initiatives at the University of Dar es Salaam (UDSM) started and were cemented with the 
              approval of the Corporate Strategic Plan (CSP) of 1994. The motivation to establish and implement the Institutional 
              Transformation Programme (ITP) was to a large extent amplified by significant socio-economic changes which occurred in 
              the country in the mid of 1980s. The socio-economic changes introduced competition in a monopolistic environment and had an
               impact on almost all spheres of the society. For example, the provision of university education was no longer monopolized by
                the public sector as private universities were being established. Although UDSM remained the largest public university in the 
                country, the trends within the country at that time not only increased 
              competition for UDSM from local universities but also from regional and international universities in search of profits.  
              </p>
              <p>
              Through ITP, there were significant achievements in terms of student enrolment, resource utilization, governance and management of the institution and adaptation and deployment of information and communication technology. Furthermore, UDSM registered tremendous achievements in research by which the volume and quality of research output increased as evidenced in the number of publications in local and international referred journals. The UDSM also celebrated its Golden Anniversary in October 2011 in which it contemplated a significant role in the last fifty years in producing and advancing knowledge as well as educating generations of men and women from the African continent and beyond. The occasion of the 50th Anniversary provided an opportunity for the University to take stock of its past efforts and seriously reflect on its future directions and aspirations. 
              </p>
